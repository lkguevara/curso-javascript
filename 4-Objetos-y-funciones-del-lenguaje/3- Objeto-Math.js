// * #1. Definición
  /*
    Math es un objeto incorporado que tiene propiedades y métodos para constantes y funciones matemáticas. No es un objeto de función.
  */

// * #2. Propiedades
  /*
    * Math.E
        Constante de Euler, la base de los logaritmos naturales, aproximadamente 2.718.
    * Math.LN2
        Logaritmo natural de 2, aproximadamente 0.693.
    * Math.LN10
        Logaritmo natural de 10, aproximadamente 2.303.
    * Math.LOG2E
        Logaritmo de E con base 2, aproximadamente 1.443.
    * Math.LOG10E
        Logaritmo de E con base 10, aproximadamente 0.434.
    * Math.PI
        Ratio de la circunferencia de un circulo respecto a su diámetro, aproximadamente 3.14159.
    * Math.SQRT1_2
        Raíz cuadrada de 1/2; Equivalentemente, 1 sobre la raíz cuadrada de 2, aproximadamente 0.707.
    * Math.SQRT2
        Raíz cuadrada de 2, aproximadamente 1.414.
  */

// * #3. Métodos

  // *Math.abs (Valor absoluto)
    console.log(Math.abs(7.8)) // Imprime: 7.8

  // *Math.ceil (Devuelve el entero más pequeño mayor o igual que un número)
    console.log(Math.ceil(7.8)) // Imprime: 8

  // *Math.floor (Devuelve el mayor entero menor que o igual a un número.)
    console.log(Math.floor(7.8)) // Imprime: 7

  // *Math.round (Redondea al más cercano inmediato)
    console.log(Math.round(7.8)) // Imprime: 8

  // *Math.sqrt (Raiz cuadrada)
    console.log(Math.sqrt(81)) // Imprime: 9

  // *Math.pow (Exponente)
    console.log(Math.pow(2,5)) // Imprime: 32

  // *Math.sign (negativo: -1, positivo: 1, neutro:0)
    console.log(Math.sign(-7.8)) // Imprime: -1

  // *Math.random (valores aleatorios entre 0 - 1)
    console.log(Math.random()) // Imprime: 0.85
    console.log(Math.round(Math.random()*1000)) // Imprime: números aleatorios entre 0 - 1000)

  // *Math.
    console.log(Math.abs(7.8)) // Imprime: 7.8

