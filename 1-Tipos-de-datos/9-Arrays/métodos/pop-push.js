//* pop() ---> Permite Eliminar el último elemento de un Array

let frutas02 = ["Manzana", "Banana", "Uva", "Granadilla"]
frutas.pop(); // Elimina granadilla del final
console.log(frutas) // [ 'Manzana', 'Banana', 'Uva' ]

//* push() ---> Permite añadir un elemento al final de un arreglo.

let frutas03 = ["Manzana", "Banana", "Uva", "Granadilla"]
frutas03.push("Fresa");
console.log(frutas03) // [ 'Manzana', 'Banana', 'Uva', 'Granadilla', 'Fresa' ]
